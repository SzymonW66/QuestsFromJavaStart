package Chapter9.ex1;

public class Point9 {
    private int X;
    private int Y;

    public Point9() {
    }

    public Point9(int x, int y) {
        X = x;
        Y = y;
    }

    public int getX() {
        return X;
    }

    public void setX(int x) {
        X = x;
    }

    public int getY() {
        return Y;
    }

    public void setY(int y) {
        Y = y;
    }
}
