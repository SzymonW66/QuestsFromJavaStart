package Chapter6.ex2;

public class MetricConventer {
    double metersToCm (double meters) {
        return meters * 100;
    }
    double metersToMm (double meters) {
        return meters * 1000;
    }
    double centimetersToMeters (double centimeters) {
        return centimeters / 100;
    }
    double milimetersToMeters (double milimeters) {
        return  milimeters / 1000;
    }
}
